static BaseType_t prvMkdirCommand(char *pcWriteBuffer,
                                  size_t xWriteBufferLen,
                                  const char *pcCommandString)
{
    char param[64];
    BaseType_t paramLen;

    /* 引数取得 */
    const char *pcParam = FreeRTOS_CLIGetParameter(
        pcCommandString, 1, &paramLen);

    if(pcParam == NULL)
    {
        snprintf(pcWriteBuffer, xWriteBufferLen,
                 "mkdir: missing operand\r\n");
        return pdFALSE;
    }

    /* コピー＆NULL終端 */
    if(paramLen >= sizeof(param))
        paramLen = sizeof(param) - 1;

    memcpy(param, pcParam, paramLen);
    param[paramLen] = '\0';

    /* フルパス生成 */
    char path[256];

    if(param[0] == '/')
    {
        snprintf(path, sizeof(path), "%s", param);
    }
    else
    {
        snprintf(path, sizeof(path), "%s/%s", pcCurrentDir, param);
    }

    /* mkdir実行 */
    FF_Error_t err = FF_MkDir(pxIOManager, path);

    if(err != FF_ERR_NONE)
    {
        snprintf(pcWriteBuffer, xWriteBufferLen,
                 "mkdir: error (%08X)\r\n", err);
    }
    else
    {
        snprintf(pcWriteBuffer, xWriteBufferLen,
                 "mkdir: created %s\r\n", path);
    }

    return pdFALSE;
}

static BaseType_t prvCatCommand(char *pcWriteBuffer,
                                size_t xWriteBufferLen,
                                const char *pcCommandString)
{
    FF_Error_t xError;
    char param[64];
    BaseType_t paramLen;

    /* 引数取得 */
    const char *pcParam = FreeRTOS_CLIGetParameter(
        pcCommandString, 1, &paramLen);

    if(pcParam == NULL)
    {
        snprintf(pcWriteBuffer, xWriteBufferLen,
                 "cat: missing file\r\n");
        return pdFALSE;
    }

    /* コピー＆NULL終端 */
    if(paramLen >= sizeof(param))
        paramLen = sizeof(param) - 1;

    memcpy(param, pcParam, paramLen);
    param[paramLen] = '\0';

    /* フルパス生成 */
    char path[128];

    if(param[0] == '/')
    {
        snprintf(path, sizeof(path), "%s", param);
    }
    else
    {
        if(strcmp(pcCurrentDir, "/") == 0)
            snprintf(path, sizeof(path), "/%s", param);
        else
            snprintf(path, sizeof(path), "%s/%s", pcCurrentDir, param);
    }

    /* ファイルオープン */
    //FF_FILE *fp = FF_Open(pxIOManager, path, FF_MODE_READ);
	FF_FILE *fp = FF_Open(pxIOManager,
	                      path,
	                      FF_MODE_READ,
	                      &xError);

    if(fp == NULL)
    {
        snprintf(pcWriteBuffer, xWriteBufferLen,
                 "cat: cannot open %s\r\n", path);
        return pdFALSE;
    }

    /* 読み込みバッファ */
    unsigned char readBuf[64];
    int readBytes;

    size_t offset = 0;

    do
    {
        readBytes = FF_Read(fp, 1, sizeof(readBuf), readBuf);

        if(readBytes > 0)
        {
            /* バッファに収まる分だけコピー */
            int toCopy = readBytes;

            if(offset + toCopy >= xWriteBufferLen)
                toCopy = xWriteBufferLen - offset - 1;

            memcpy(pcWriteBuffer + offset, readBuf, toCopy);
            offset += toCopy;

            if(offset >= xWriteBufferLen - 1)
                break;
        }

    } while(readBytes > 0);

    pcWriteBuffer[offset] = '\0';

    FF_Close(fp);

    return pdFALSE;
}

static BaseType_t prvCpCommand(char *pcWriteBuffer,
                               size_t xWriteBufferLen,
                               const char *pcCommandString)
{
    char src[64], dst[64];
    BaseType_t len1, len2;

    /* 引数取得 */
    const char *p1 = FreeRTOS_CLIGetParameter(pcCommandString, 1, &len1);
    const char *p2 = FreeRTOS_CLIGetParameter(pcCommandString, 2, &len2);

    if(p1 == NULL || p2 == NULL)
    {
        snprintf(pcWriteBuffer, xWriteBufferLen,
                 "cp: src dst\r\n");
        return pdFALSE;
    }

    /* コピー */
    if(len1 >= sizeof(src)) len1 = sizeof(src)-1;
    if(len2 >= sizeof(dst)) len2 = sizeof(dst)-1;

    memcpy(src, p1, len1); src[len1] = '\0';
    memcpy(dst, p2, len2); dst[len2] = '\0';

    /* パス生成 */
    char pathSrc[128], pathDst[128];

    if(src[0] == '/')
        snprintf(pathSrc, sizeof(pathSrc), "%s", src);
    else
        snprintf(pathSrc, sizeof(pathSrc), "%s/%s", pcCurrentDir, src);

    if(dst[0] == '/')
        snprintf(pathDst, sizeof(pathDst), "%s", dst);
    else
        snprintf(pathDst, sizeof(pathDst), "%s/%s", pcCurrentDir, dst);

    /* open src */
    FF_Error_t err;
    FF_FILE *fpSrc = FF_Open(pxIOManager, pathSrc, FF_MODE_READ, &err);

    if(fpSrc == NULL)
    {
        snprintf(pcWriteBuffer, xWriteBufferLen,
                 "cp: open src error (%08X)\r\n", err);
        return pdFALSE;
    }

    /* open dst */
    FF_FILE *fpDst = FF_Open(pxIOManager, pathDst,
                             FF_MODE_WRITE | FF_MODE_CREATE | FF_MODE_TRUNCATE,
                             &err);

    if(fpDst == NULL)
    {
        FF_Close(fpSrc);
        snprintf(pcWriteBuffer, xWriteBufferLen,
                 "cp: open dst error (%08X)\r\n", err);
        return pdFALSE;
    }

    /* コピー */
    uint8_t buf[128];
    int32_t r;

    do
    {
        r = FF_Read(fpSrc, 1, sizeof(buf), buf);

        if(r > 0)
        {
            int32_t w = FF_Write(fpDst, 1, r, buf);

            if(w != r)
            {
                snprintf(pcWriteBuffer, xWriteBufferLen,
                         "cp: write error\r\n");
                break;
            }
        }

    } while(r > 0);

    FF_Close(fpSrc);
    FF_Close(fpDst);

    snprintf(pcWriteBuffer, xWriteBufferLen,
             "cp: done\r\n");

    return pdFALSE;
}

