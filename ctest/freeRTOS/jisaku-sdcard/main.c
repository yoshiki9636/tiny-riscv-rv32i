static BaseType_t prvCdCommand(char *pcWriteBuffer,
                               size_t xWriteBufferLen,
                               const char *pcCommandString)
{
    const char *param;
    BaseType_t len;

    param = FreeRTOS_CLIGetParameter(pcCommandString, 1, &len);

    if(param == NULL)
    {
        snprintf(pcWriteBuffer, xWriteBufferLen, "%s\r\n", pcCurrentDir);
        return pdFALSE;
    }

    char input[256];
    strncpy(input, param, len);
    //input[len] = '\0';

    char newPath[256];
    vNormalizePath(newPath, input);

    FF_DirEnt_t xDir;

    if(FF_FindFirst(pxIOManager, &xDir, newPath) != FF_ERR_NONE)
    {
        snprintf(pcWriteBuffer, xWriteBufferLen, "cd: no such dir: %s\r\n", xDir);
        return pdFALSE;
    }

    if(!(xDir.ucAttrib & FF_FAT_ATTR_DIR))
    {
        snprintf(pcWriteBuffer, xWriteBufferLen, "cd: not a dir: %s\r\n", xDir);
        return pdFALSE;
    }

    strncpy(pcCurrentDir, newPath, sizeof(pcCurrentDir));

    snprintf(pcWriteBuffer, xWriteBufferLen, "OK\r\n");
    return pdFALSE;
}

